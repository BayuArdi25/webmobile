<?php
// Koneksi ke database
include 'config/config.php';

// Mulai sesi
session_start();

// Periksa apakah tombol login diklik
if (isset($_POST['login'])) {
    $email = $_POST['email'];
    $password = $_POST['password'];

    // Query untuk memeriksa email
    $query = "SELECT * FROM users WHERE email = ?";
    if ($stmt = $conn->prepare($query)) {
        $stmt->bind_param("s", $email); // Bind email sebagai string
        $stmt->execute();
        $result = $stmt->get_result();

        // Periksa apakah email ditemukan
        if ($result->num_rows > 0) {
            $user = $result->fetch_assoc();

            // Verifikasi password
            if (password_verify($password, $user['password'])) {
                // Set sesi login
                $_SESSION['username'] = $user['username'];
                $_SESSION['email'] = $user['email'];

                // Redirect ke dashboard
                header("Location: index.php");
                exit;
            } else {
                echo "<div class='alert alert-danger'>Password salah!</div>";
            }
        } else {
            echo "<div class='alert alert-danger'>Email tidak ditemukan!</div>";
        }
        $stmt->close();
    } else {
        echo "<div class='alert alert-danger'>Terjadi kesalahan pada query: " . $conn->error . "</div>";
    }
}
